const express = require('express');
const router = express.Router();
const {Graphical_Method} = require("../controllers/Graphical_Method");
const {Bisection_Method} = require("../controllers/Bisection_Method");
const {FalsePosition_Method} = require("../controllers/FalsePosition_Method");
const {Register , Login , requireLogin} = require("../controllers/Login_Register");
const {AddEquation} = require("../controllers/AddEquation");
const {OnePoint_Iterration} = require("../controllers/OnePoint_Iterration");
const {GetEquation} = require("../controllers/GetEquation");
const {NewTonRaphson} = require("../controllers/NewTonRaphson");
const {Secant_Method} = require("../controllers/Secant_Method");
const {Cramer} = require("../controllers/Cramer");
router.post("/Register" , Register);
router.post("/Login" , Login);
router.use(requireLogin);
router.post("/Graphical" , Graphical_Method);
router.post("/Bisection" , Bisection_Method);
router.post("/FalsePosition" , FalsePosition_Method);
router.post("/AddEquation" , AddEquation);
router.post("/OnePoint" , OnePoint_Iterration);
router.get("/Getequation", GetEquation);
router.post("/NewTonRaphson", NewTonRaphson);
router.post("/Secant_Method", Secant_Method);
router.post("/Cramer" , Cramer);
module.exports = router;